    private void filltable() {
        model.setRowCount(0);
        ArrayList<GiamGiaKH> Gkh = dao.getall();
        for (GiamGiaKH g : Gkh) {
            model.addRow(new Object[]{g.getMaTKH(),
                g.getMaKH(),
                g.isVaitro() ? "Nhân Viên" : "Quản Lý",
                g.getGiatriGG(),
                g.getDieuKienGG(),
                g.getMota(),
                g.getHinhthuc()});

        }
    }

    private void Clicktable() {
        int index = tbl_bangKH.getSelectedRow();
        txt_maKH.setText(tbl_bangKH.getValueAt(index, 1).toString());
        Boolean kh = tbl_bangKH.getValueAt(index, 2).toString().equalsIgnoreCase("Quản Lý") ? true : false;
        rdo_quanly.setSelected(kh);
        rdo_nhanvien.setSelected(!kh);
        txt_gtgg.setText(tbl_bangKH.getValueAt(index, 3).toString());
        txt_dkgg.setText(tbl_bangKH.getValueAt(index, 4).toString());
        txt_mota.setText(tbl_bangKH.getValueAt(index, 5).toString());
        txt_hinhthuc.setText(tbl_bangKH.getValueAt(index, 6).toString());
    }

    private GiamGiaKH getFrom() {
        GiamGiaKH sv = new GiamGiaKH();
        sv.setMaKH(Integer.parseInt(txt_maKH.getText()));
        sv.setVaitro(rdo_nhanvien.isSelected() ? true : false);
        sv.setGiatriGG(txt_gtgg.getText());
        sv.setDieuKienGG(txt_dkgg.getText());
        sv.setMota(txt_mota.getText());
        sv.setHinhthuc(txt_hinhthuc.getText());
        return sv;
    }

