    private void search() {
        String ten = txt_timKiem.getText();
        ArrayList<SanPham> listFind = new ArrayList<>();
        for (int i = 0; i < service.getAllSanPham().size(); i++) {
            SanPham nh = service.getAllSanPham().get(i);
            if (nh.getTenSP().contains(ten)) {
                listFind.add(nh);
            }
        }

        if (txt_timKiem.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin để tìm kiếm");
            return;
        } else {

            if (listFind.size() > 0) {
                model.setRowCount(0);

                for (SanPham sp : listFind) {
                    model.addRow(new Object[]{
                        sp.getId(),
                        sp.getTenSP(),
                        sp.getTl(),
                        sp.getKt(),
                        sp.getMs(),
                        sp.getCl(),
                        sp.getDonGia(),
                        sp.getSoLuongTon(),
                        sp.getTrangThai() == 0 ? "Hết Hàng" : "Còn Hàng",});
                }
            } else {
                JOptionPane.showMessageDialog(this, "Rất tiếc không tìm thấy sản phẩm này :>>");
            }
        }

    }
