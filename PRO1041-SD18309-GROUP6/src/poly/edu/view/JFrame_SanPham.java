    private void add() throws ParseException {
        try {
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            if (validates()) {
                SanPham nh = getForm();
                if (service.add(nh) != null) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    loadDataTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nút add");
        }
    }

    private void update() throws ParseException {
        try {
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn cập nhật không?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            if (validates()) {
                SanPham sp = getForm();
                int index = tbl_list.getSelectedRow();
                if (service.update(sp) != null && index >= 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                    loadDataTable();
                    if (tbl_list.getRowCount() >= 0) {
                        tbl_list.setRowSelectionInterval(1, 1);
                        showDetail();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nút update");
        }
    }

    private void delete() throws ParseException {
        try {
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            if (validates()) {
                SanPham nh = getForm();
                int index = tbl_list.getSelectedRow();
                int masp = Integer.parseInt(txt_maSP.getText());
                if (service.delete(index, masp) != null && index >= 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    loadDataTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nút delete");
        }
    }

