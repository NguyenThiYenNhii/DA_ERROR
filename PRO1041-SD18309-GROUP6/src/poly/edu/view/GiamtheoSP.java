    private void fillbang() {
model.setRowCount(0);
ArrayList<GiamTheoSPCL> sp = dao.getall();
        for (GiamTheoSPCL gsp : sp) {
            model.addRow(new Object[]{gsp.getMaSP(),gsp.getGiaTrigg(),gsp.getDieuKienGG(),gsp.getMoTa()});
        }
    }
     private GiamTheoSPCL getFrom(){
   GiamTheoSPCL sv = new GiamTheoSPCL();
 sv.setMaSP(txt_masp.getText());
    sv.setDieuKienGG(txt_dieukiengg.getText());
    sv.setGiaTrigg(txt_giatrigg.getText());
    sv.setMoTa(txt_mota.getText());
   return sv;
   }
 private void Clicktable() {
        int index = tbl_bang.getSelectedRow();
    txt_masp.setText(tbl_bang.getValueAt(index, 0).toString());
    txt_dieukiengg.setText(tbl_bang.getValueAt(index, 1).toString());
    txt_giatrigg.setText(tbl_bang.getValueAt(index, 2).toString());
     txt_mota.setText(tbl_bang.getValueAt(index, 3).toString());
    }
